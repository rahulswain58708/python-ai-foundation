#Q1.Print numbers from 1 to 10 using for loop.
for i in range(1,11):
    print(i)
#Q2.Print numbers from 10 to 1 using for loop.
for i in range(10,0,-1):
    print(i)
#Q3.Print even numbers from 1 to 20.
for i in range(1,21):
    if i % 2 == 0:
        print(i)
#Q4.Print odd numbers from 1 to 20.
for i in range(1,21):
    if i % 2 != 0:
        print(i)
