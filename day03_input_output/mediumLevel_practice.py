# Take two numbers as input and print:
# sum
# difference
# product
num1 = int(input("Enter first number:"))
num2 = int(input("Enter second number:"))
print(f"sum = {num1+num2}")
print(f"diff = {num1-num2}")
print(f"product = {num1*num2}")
# Take length and breadth as input and print the area of a rectangle.
length = float(input("Enter Length:"))
breadth = float(input("Enter breadth:"))
area = length * breadth
print(f"Area of Rectangle = {area}")
# Take price and quantity as input and print the total cost.
price = float(input("Enter Price:"))
quantity = int(input("Enter quantity:"))
print(f"total cost = {price * quantity}")